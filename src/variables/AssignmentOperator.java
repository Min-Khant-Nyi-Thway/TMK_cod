package variables;

public class AssignmentOperator {
    
    public static void main(String[] args) {
        
        int num = 10;
        num+=10;
        System.out.println("Operation-1 num is " + num);

        num-= 5;
        System.out.println("Operation-2 num is " + num);

        num*=2;
        System.out.println("Operation-3 num is "+ num);

        num/=3;
        System.out.println("Operation-4 num is "+ num);

        num%=3;
        System.out.println("Operation-5 num is "+ num);

    }

}
